  static Unite _parseUnite(String unite) {
    switch (unite) {
      case 'piece':
        return Unite.piece;
      case 'metre':
        return Unite.metre;
      case 'rouleau':
        return Unite.rouleau;
      case 'boite':
        return Unite.boite;
      case 'pack':
        return Unite.pack;
      case 'kit':
        return Unite.kit;
      default:
        return Unite.piece;
    }
  }
